/**
 * ثابت مشترك للتعبير النمطي الخاص بحروف التحكم (Control Characters).
 *
 * كان هذا الـ regex مُعرَّفاً في 8 ملفات `securityUtils.ts` مختلفة بنسختين
 * فقط تختلفان في حجم حرف `F/f` داخل النطاق (بلا أثر عملي على التطابق):
 *   - /[\u0000-\u001F\u007F]/g
 *   - /[\u0000-\u001f\u007f]/g
 *
 * الآن نعرّفه مرة واحدة هنا؛ كل ملف يستورده ويتركبه بالطريقة التي تناسب سياقه
 * (بدون مسافة بديلة، أو بمسافة واحدة، أو مع `\s+` collapse...).
 *
 * يجب ألا يفسّر أي ملف هذا الـ regex كـ "سياسة تنظيف" — هو مجرد تعريف للأحرف
 * التي نعتبرها "تحكم" ولا يجب أن تمر للمدخلات.
 */
export const CONTROL_CHARS_REGEX = /[\u0000-\u001F\u007F]/g;
