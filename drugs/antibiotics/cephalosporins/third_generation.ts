const vialMg = (c: string) => (c === '2gm' || c === '2g' ? 2000 : c === '1gm' || c === '1g' ? 1000 : c === '500mg' ? 500 : 0);

const ceftriaxoneDose = (weightKg: number, ageMonths: number, concentration: string, route: 'IV' | 'IM'): string => {
	if (ageMonths < 1) return 'حديثي الولادة: جرعات خاصة—يلزم بروتوكول.';
	const v = vialMg(concentration);
	if (!v) return 'جرعة غير متاحة لهذا التركيز.';
	const isAdult = ageMonths >= 144 || weightKg >= 40;
	const mgPerMl = route === 'IV' ? 100 : 250;
	const dailyMin = isAdult ? 1000 : clamp(weightKg * 50, 0, 4000);
	const dailyMax = isAdult ? 2000 : clamp(weightKg * 100, 0, 4000);
	const mlMin = Math.round((dailyMin / mgPerMl) * 10) / 10;
	const mlMax = Math.round((dailyMax / mgPerMl) * 10) / 10;
	if (Math.abs(mlMin - mlMax) < 0.1) return `${toAr(mlMin)} مل مرة يومياً لمدة ٥–١٤ يوم`;
	return `${toAr(mlMin)}–${toAr(mlMax)} مل مرة يومياً لمدة ٥–١٤ يوم`;
};

const cefotaximeDose = (weightKg: number, ageMonths: number, concentration: string, route: 'IV' | 'IM'): string => {
	if (ageMonths < 1) return 'حديثي الولادة: جرعات خاصة—يلزم بروتوكول.';
	const v = vialMg(concentration);
	if (!v) return 'جرعة غير متاحة لهذا التركيز.';
	const isAdult = ageMonths >= 144 || weightKg >= 40;
	const mgPerMl = route === 'IV' ? 125 : 250;
	const doseMin = isAdult ? 1000 : clamp(weightKg * 50, 0, 2000);
	const doseMax = isAdult ? 1000 : clamp(weightKg * 75, 0, 2000);
	const mlMin = Math.round((doseMin / mgPerMl) * 10) / 10;
	const mlMax = Math.round((doseMax / mgPerMl) * 10) / 10;
	if (Math.abs(mlMin - mlMax) < 0.1) return `${toAr(mlMin)} مل كل ٨ ساعات لمدة ٧–١٤ يوم`;
	return `${toAr(mlMin)}–${toAr(mlMax)} مل كل ٨ ساعات لمدة ٧–١٤ يوم`;
};

