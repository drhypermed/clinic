const ceftriaxone = (conc: string, route: 'IV' | 'IM') => (w: number, a: number) => ceftriaxoneDose(w, a, conc, route);
const cefotaxime = (conc: string, route: 'IV' | 'IM') => (w: number, a: number) => cefotaximeDose(w, a, conc, route);
const ceftazidime = (conc: string, route: 'IV' | 'IM') => (w: number, a: number) => ceftazidimeDose(w, a, conc, route);

